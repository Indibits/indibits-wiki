<div class="container table-bordered">
<h1>Installation</h1><br>
<blockquote>
<ol>
<li>Extract contents of the application into your web host.</li>
<li>Open <strong>application/config/config.php</strong>. And set the base url. Example:<br><br>
	<blockquote>
		<code>$config['base_url']	= 'http://localhost/';</code>
	</blockquote></li>
<li>Then go to that url in the browser. And proceed with the installation procedure.</li>
<li>Then the same url will be set as the home page of your wiki application.</li>
</ol>
</blockquote>
</div><br>

<div class="container table-bordered">
<h1>Change Theme</h1><br>
<blockquote>
<ol>
<li>Login to your <strong>owner</strong> account.</li>
<li>Go to <strong>help->settings</strong>.</li>
<li>Now you can choose between <em>12</em> different themes available. Choose any of them depending on your wiki application.</li>
</ol>
</blockquote>
</div><br>

<div class="container table-bordered">
<h1>Creating new Wiki</h1><br>
<blockquote>
<ol>
<li>Login to your editor/owner account.</li>
<li>Go to <strong>wikis</strong> tab in the dashboard and click on <strong>New Wiki</strong></li>
</ol>
</blockquote>
</div><br>

<div class="container table-bordered">
<h1>Edit a Wiki</h1><br>
<blockquote>
<ol>
<li>Login to your editor/owner account.</li>
<li>Go to <strong>wikis</strong> tab in the dashboard and click on <strong>Edit Mode</strong> to switch to edit mode from view mode.</li>
<li>Click on the wiki which you wan't to edit.</li>
</ol>
</blockquote>
</div><br>

<div class="container table-bordered">
<h1>Generate new editor</h1><br>
<blockquote>
<ol>
<li>Login to your <strong>owner</strong> account.</li>
<li>Go to <strong>Manage Accounts</strong> tab in the dashboard.</li>
<li>Enter details of the new user. Then click on <strong>Generate new Editor</strong>.</li>
<li>A unique username will be generated. If you wan't to ignore the generated details, click close.</li>
<li>Once the generated details are saved, a mail is sent to the new user with all the login details.</li>
</ol>
</blockquote>
</div><br>

<div class="container table-bordered">
<h1>Manage account type</h1><br>
<blockquote>
<ol>
<li>Login to your <strong>owner</strong> account.</li>
<li>Go to <strong>Manage Accounts</strong> tab in the dashboard.</li>
<li>Click any of the existing users and change the type and save.</li>
</ol>
</blockquote>
</div><br>

